def multiplicative_iterator(M):
    """
    Iterate over elements of ``M`` using multiplicative generator combinations.

    The object ``M`` is expected to provide:

    - ``M.gens()``: a list/sequence of generators for the finite group.
    - ``M(1)``: a callable constructor returning the identity element.
    - Each generator ``g`` must implement ``g.multiplicative_order()``.

    The iterator yields each group element produced by multiplying generator
    powers where the exponent of generator ``g`` runs from ``0`` up to
    ``g.multiplicative_order() - 1``.

    INPUT:

    - ``M`` -- a finite multiplicative group-like object

    OUTPUT:

    - elements of ``M`` in a deterministic order

   If not all generators have finite multiplicative order, an
   ``ArithmeticError`` is raised.
    """
    from sage.rings.infinity import infinity

    generators = M.gens()
    if len(generators) == 0:
        yield M(1)
        return

    # For each generator, compute the maximum count (order-1).
    limits = [g.multiplicative_order() for g in generators]
    for i in range(len(limits)):
        if limits[i] is infinity:
            raise ArithmeticError("%s is not finite." % M)

        # We increment "counts" from 0 through (order-1), inclusive.
        limits[i] -= 1

    current = M(1)
    yield current

    # `counts` is the current exponent vector (for an odometer).
    counts = [0] * len(generators)
    while counts != limits:
        current = current * generators[0]
        counts[0] = counts[0] + 1

        i = 0
        # Carry to the next generator when the current one overflows.
        while i < len(counts) - 1 and counts[i] > limits[i]:
            counts[i] = 0
            counts[i + 1] = counts[i + 1] + 1
            current = current * generators[i + 1]
            i += 1
        yield current


def abelian_iterator(M):
    """
    Iterate over elements of ``M`` using additive generator combinations.

    The object ``M`` is expected to provide:

    - ``M.gens()``: a list/sequence of generators for the finite abelian group.
    - ``M(0)``: a callable constructor returning the additive identity element.
    - Each generator ``g`` must implement ``g.additive_order()``.

    The iterator yields each group element produced by taking sums of
    generator multiples where the coefficient of generator ``g`` runs from
    ``0`` up to ``g.additive_order() - 1``.

    INPUT:
        - ``M`` -- a finite additive group-like object.

    OUTPUT:
        - Yields elements of ``M`` in a deterministic order.

    ASSUMPTIONS:
        - All generators have finite additive order; otherwise an
          ``ArithmeticError`` is raised.
    """
    from sage.rings.infinity import infinity

    generators = M.gens()
    if len(generators) == 0:
        yield M(0)
        return

    # For each generator, compute the maximum count (order-1).
    limits = [g.additive_order() for g in generators]
    for i in range(len(limits)):
        if limits[i] is infinity:
            raise ArithmeticError("%s is not finite." % M)

        # We increment "counts" from 0 through (order-1), inclusive.
        limits[i] = limits[i] - 1

    current = M(0)
    yield current

    # `counts` is the current coefficient vector (for an odometer).
    counts = [0] * len(generators)
    while counts != limits:
        counts[0] = counts[0] + 1
        current = current + generators[0]
        i = 0

        # Carry to the next generator when the current one overflows.
        while i < len(counts) - 1 and counts[i] > limits[i]:
            counts[i] = 0
            counts[i + 1] = counts[i + 1] + 1
            current = current + generators[i + 1]
            i += 1
        yield current
